function item1() {
    let val = "https://is.gd/bvMq7v";
    const selBox = document.createElement("textarea");
    selBox.value = val;
    document.body.appendChild(selBox);
    selBox.focus();
    selBox.select();
    document.execCommand("copy");
    document.body.removeChild(selBox);
    alert("url copied to clipboard");
}
